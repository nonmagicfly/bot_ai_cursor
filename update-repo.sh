echo "   2. –ï—Å–ª–∏ –±—ã–ª–∏ –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ docker-compose —Ñ–∞–π–ª–∞—Ö, –ø–µ—Ä–µ—Å–æ–±–µ—Ä–∏—Ç–µ:"
echo "      docker-compose -f docker-compose.yml -f docker-compose.monitoring.yml build"
echo ""
echo "   3. –ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç–µ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã:"
