if [ ! -d .git ]; then
    echo "‚ùå –¢–µ–∫—É—â–∞—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –Ω–µ —è–≤–ª—è–µ—Ç—Å—è git —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–µ–º"
    echo ""
    echo "–í–∞—Ä–∏–∞–Ω—Ç—ã:"
    echo "  1. –ï—Å–ª–∏ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π —É–∂–µ –∫–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω, –ø–µ—Ä–µ–π–¥–∏—Ç–µ –≤ –µ–≥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é:"
    echo "     cd bot_ai_cursor"
    echo "     ./update-repo.sh"
    echo ""
    echo "  2. –ï—Å–ª–∏ —Ö–æ—Ç–∏—Ç–µ –ø–µ—Ä–µ—É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —Å –Ω—É–ª—è:"
    echo "     cd ~"
    echo "     rm -rf bot_ai_cursor"
    echo "     git clone https://github.com/nonmagicfly/bot_ai_cursor.git"
    echo "     cd bot_ai_cursor"
    exit 1
